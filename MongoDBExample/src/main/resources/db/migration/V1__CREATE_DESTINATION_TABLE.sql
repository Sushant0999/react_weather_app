CREATE TABLE Destination (
  name VARCHAR(255),
  accountId UUID,
  createdBy VARCHAR(255),
  createdByAuthId VARCHAR(255),
  createdOn DATE,
  endpointId VARCHAR(255),
  lastModifiedBy VARCHAR(255),
  lastModifiedByAuthId VARCHAR(255),
  lastModifiedOn VARCHAR(255),
  serviceId VARCHAR(255),
  storageProfileId VARCHAR(255),
  type VARCHAR(255),
  url VARCHAR(255)
);
